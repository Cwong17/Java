public class Operator {
    public static void main(String[] args){
        int num1 = 20;
        int num2 = 2;
        int sum;
        //add
        sum = num1 + num2;
        System.out.println(sum);
        //sub
        sum = num1 - num2;
        System.out.println(sum);
        //multi
        sum = num1 * num2;
        System.out.println(sum);
        //div
        sum = num1 / num2;
        System.out.println(sum);
        //mix
        sum = (num1 + num2) * num1;
        System.out.println(sum);
    }
}
